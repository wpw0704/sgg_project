#include <stdio.h>

// 一个5位数，判断它是不是回文数。即12321是回文数，个位与万位相同，十位与千位相同。

int main()
{
    int a, b, c, d, e, x;
    printf("请输入不多于5位的正整数: \n");
    scanf("%d", &x);
    a = x / 10000;        /*分解出万位*/
    b = x % 10000 / 1000; /*分解出千位*/
    c = x % 1000 / 100;   /*分解出百位*/
    d = x % 100 / 10;     /*分解出十位*/
    e = x % 10;           /*分解出个位*/
    if (a == b - 1 && b == c - 1 && a == e && b == d)
    {
        printf("是回文数 \n");
    }
    else
    {
        printf("不是是回文数 \n");
    }

    return 0;
}