#include <stdio.h>

int main(){
  // 变量: 是一个容器,用来存储数据的
  // 变量的声明语法: 数据类型名 变量名;
  // 例子:
  // 变量声明方式1: 先声明变量,然后再赋值
  int num; // 声明了一个int类型的变量,起名为num
  num = 20; // 为变量num赋值为20,往num这个容器中存储了一个整数:20

  // 变量声明方式2:声明变量的同时直接赋值
  int num2 = 30; // 声明了一个int类型的变量,起名为num2,并且赋值为30

  // 变量声明方式3:一次性声明多个变量,分别赋值

  int a = 10, b = 20, c = 30; // 声明了三个int类型的变量,分别起名为a,b,c,并且分别赋值

  return 0;
}